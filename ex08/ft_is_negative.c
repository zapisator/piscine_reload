void	ft_putchar(char c);

void	ft_is_negative(int nb)
{
	if ((nb == -2147483648) || (nb < 0))
		ft_putchar('N');
	else if ((nb == 2147483647) || (nb < 2147483647))
		ft_putchar('P');
}
