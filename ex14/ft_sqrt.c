int		ft_sqrt(int nb)
{
	int i;

	i = 1;
	if ((nb == 2147483647) || (nb > 2147395600) || (nb <= 0))
		return (0);
	else
	{
		while (i <= 46340)
		{
			if (i * i == nb)
				return (i);
			else
				i++;
		}
		return (0);
	}
}
